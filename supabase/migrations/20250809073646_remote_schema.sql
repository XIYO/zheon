CREATE OR REPLACE FUNCTION "public"."check_existing_summary"("p_youtube_url" "text") RETURNS bigint
    LANGUAGE "plpgsql" SECURITY DEFINER
    AS $$
DECLARE
    existing_summary_id BIGINT;
BEGIN
    -- 기존 요약이 있는지 확인 (url 컬럼 사용)
    SELECT id INTO existing_summary_id 
    FROM summary 
    WHERE url = p_youtube_url;
    
    IF existing_summary_id IS NOT NULL THEN
        -- 기존 요약이 있으면 created_at과 last_modified_at을 업데이트하여 최상단으로 이동
        UPDATE summary 
        SET created_at = NOW(),
            last_modified_at = NOW()
        WHERE id = existing_summary_id;
        
        RETURN existing_summary_id;
    END IF;
    
    RETURN NULL; -- 새로운 요약 생성 필요
END;
$$;


ALTER FUNCTION "public"."check_existing_summary"("p_youtube_url" "text") OWNER TO "postgres";


